# ╔═╡ de19054c-ab02-48cb-858a-0565dffc1f31
md"
In order to perform Simulated Annealing algorithm, one must specify the 
solution and the neighborhood of the solution.
"

# ╔═╡ e2721d55-a6f2-46c9-a6a1-58549c8b0177
function generate_solution(𝒮 :: Vector{Any})
	𝓈 = []
	𝓃 = length(𝒮)
	while true
		choosen_index = rand(1:𝓃)
		choosen_city = 𝒮[choosen_index]
		𝓈 = choosen_city ∉ 𝓈 ? 𝓈 ∪ [choosen_city] : 𝓈
		𝔫 = length(𝓈)
		if 𝔫 ≡ 𝓃
			break
		end
	end
	return 𝓈
end

