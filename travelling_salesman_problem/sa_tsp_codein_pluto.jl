# Example of euclidean distance
begin
	u⃗ = City(1, 1)
	v⃗ = City(2, 2)
	example_euclidean_distance = euclidean_distance(u⃗, v⃗)
	example_euclidean_distance
end

# ╔═╡ 0fc6779f-6827-48f4-ac8c-f3e199dd6fdf
md"
Let $\sigma : \mathbb{N} \longrightarrow \mathbb{N}$ such that $\sigma$ is bijection, 
and let $f : \mathbb{R}^n \longrightarrow \mathbb{R}$ be the objective function of the 
problem. In this case, the cost function is the total distance of the route 
$X = \{\vec{c}_{\sigma(i)} | \text{for } i = 1, 2, \ldots, n\}$, thus.

$$f(X) = \sum_{i = 1}^{n-1} \Delta_{{\vec{c}_{\sigma(i)}}, \vec{c}_{\sigma(i+1)}} + \Delta_{\vec{c}_{\sigma(n)}, \vec{c}_{\sigma(1)}}$$
"

# ╔═╡ 22f23475-e6c1-464c-8061-76a7ec7eebc3
function circuit_length(𝓈 :: Vector{Any})
	path_length = 0
	𝓃 = length(𝓈)
	𝓃⃗ = 1:𝓃-1 # 𝓃⃗ = [1, 2, … , 𝓃 - 1]
	for i ∈ 𝓃⃗
		path_length += euclidean_distance(𝓈[i], 𝓈[i + 1])
	end
	path_length += euclidean_distance(𝓈[𝓃], 𝓈[1])
	return path_length
end

