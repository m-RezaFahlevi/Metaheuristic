# ╔═╡ 1c5e9ba8-e9b1-4fe2-a269-f8bd13f0b83c
md"
The distance between two point, for example, $\vec{u} = (x_1, y_1)$ 
and $\vec{v} = (x_2, y_2),$ can be calculated by using euclidean distance equation as follows.

$$\Delta_{\vec{u}, \vec{v}} = \sqrt{(x_1 - x_2)^2 + (y_1 - y_2)^2}$$
"

# ╔═╡ 66e0698c-dfe8-4bc2-8ad9-b59275f1bb54
function euclidean_distance(𝓊⃗ :: City, 𝓋⃗ :: City)
	u² = abs(𝓊⃗.x_coord - 𝓋⃗.x_coord)^2
	v² = abs(𝓊⃗.y_coord - 𝓋⃗.y_coord)^2
	Δ = √(u² + v²)
	return Δ
end

